    public ArrayList<Carta> crearBaraja() throws IOException {
        Random rand = new Random();
        ArrayList<Carta> cartas = new ArrayList<>();
        File archivos = new File("imagenes");
        int id=0;
        if(archivos.isDirectory()){
            for(File a : archivos.listFiles()){
               BufferedImage imagenActual = ImageIO.read(a);
               ImageIcon img = new ImageIcon(imagenActual);
               Carta carta1= new Carta(img,id,rand.nextInt(20)+1);
               Carta carta2= new Carta(img,id,rand.nextInt(20)+1);
               cartas.add(carta1);
               cartas.add(carta2);
               id++;
            }
        }
        Collections.sort(cartas);
        return cartas;
    }
    private void revolver(ArrayList<Carta> baraja){
        Random rand = new Random();
        for(Carta c: baraja){
            c.setBocaArriba(false);
            c.setRandom(rand.nextInt(20)+1);
        }
        Collections.sort(baraja);
    }
    private void voltear(JLabel label,int pos){
        System.out.println(par);
        if(!inicio){
            return ;
        }
        else{
            if(par<2){
                if(par==0){auxLabel=label;}
                Carta cartaActual = cartasB.get(pos);
                ImageIcon foto = cartaActual.getImagen();
                Icon icono = new ImageIcon(foto.getImage().getScaledInstance(label.getWidth(),label.getHeight(),Image.SCALE_DEFAULT));
                label.setIcon(icono);
                label.repaint();
                System.out.println("Ya repinte");
                if(par>0){
                    auxLabel1=label;
                    carta2=cartaActual.getId();
                }
                else{
                carta1=cartaActual.getId();
                }
                cartaActual.setBocaArriba(true);
                par++;
            }    
        }   
    }
